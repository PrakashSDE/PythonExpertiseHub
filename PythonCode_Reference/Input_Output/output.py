# f string
name = "python"
version = 3.12
print(f"Hello, my name is {name} and I am {version} years old.")

# format
name = "python"
version = 3.12
print("Hello, my name is {} and I am {} years old.".format(name, version))

# % operator
name = "python"
version = 3.12
a = ("Hello, my name is %s and I am %d years old." % (name, version))
print(a)


